package basesHibernate;

public class Zone {
		private int id;
		private int debut;
		private int fin ;
		private int prix;
		
		
		
		public Zone(){
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public int getDebut() {
			return debut;
		}
		public void setDebut(int debut) {
			this.debut = debut;
		}
		public int getFin() {
			return fin;
		}
		public void setFin(int fin) {
			this.fin = fin;
		}
		public int getPrix() {
			return prix;
		}
		public void setPrix(int prix) {
			this.prix = prix;
		}
		
		
		
}	
